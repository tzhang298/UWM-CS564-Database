SELECT
  tmp3.dept,
  tmp3.yr,
  tmp3.mo,
  tmp3.monthlysales,
  tmp3.contribution,
  tmp4.cumulative_sales
FROM (SELECT
        tmp1.dept,
        tmp1.year                                                      yr,
        tmp1.month                                                     mo,
        tmp1.sales                                                     monthlysales,
        round(cast(tmp1.sales / tmp2.total_sales * 100 AS NUMERIC), 2) contribution
      FROM (SELECT
              dept,
              sum(weeklysales)                sales,
              EXTRACT(YEAR FROM weekdate)  AS year,
              EXTRACT(MONTH FROM weekdate) AS month
            FROM hw2.sales
            GROUP BY dept, EXTRACT(YEAR FROM weekdate), EXTRACT(MONTH FROM weekdate)) tmp1
        JOIN (SELECT
                dept,
                sum(weeklysales) total_sales
              FROM hw2.sales
              GROUP BY dept) tmp2 ON tmp1.dept = tmp2.dept
      ORDER BY dept, tmp1.year, tmp1.month) tmp3
  JOIN (SELECT
          tmp2.dept,
          tmp2.year                                  yr,
          tmp2.month                                 mo,
          round(cast(sum(tmp1.sales) AS NUMERIC), 2) cumulative_sales
        FROM (SELECT
                dept,
                EXTRACT(YEAR FROM weekdate)  AS year,
                EXTRACT(MONTH FROM weekdate) AS month,
                sum(weeklysales)                sales
              FROM hw2.sales
              GROUP BY dept, EXTRACT(YEAR FROM weekdate), EXTRACT(MONTH FROM weekdate)) tmp1
          JOIN (SELECT
                  hw2.sales.dept,
                  EXTRACT(YEAR FROM weekdate)  AS year,
                  EXTRACT(MONTH FROM weekdate) AS month
                FROM hw2.sales
                GROUP BY dept, EXTRACT(YEAR FROM weekdate), EXTRACT(MONTH FROM weekdate)) tmp2
            ON tmp1.dept = tmp2.dept AND tmp2.year >= tmp1.year AND tmp2.month >= tmp1.month
        GROUP BY tmp2.dept, tmp2.year, tmp2.month
        ORDER BY tmp2.dept, tmp2.year, tmp2.month) tmp4
    ON tmp3.dept = tmp4.dept AND tmp3.yr = tmp4.yr AND tmp3.mo = tmp4.mo
  JOIN (SELECT tmp2.dept
        FROM (SELECT
                hw2.sales.store,
                hw2.sales.dept,
                sum(weeklysales) sales
              FROM hw2.sales
              GROUP BY hw2.sales.store, hw2.sales.dept) tmp2
          JOIN (SELECT
                  hw2.sales.dept,
                  hw2.sales.store,
                  max(size) size
                FROM hw2.sales
                  JOIN hw2.stores ON hw2.sales.store = hw2.stores.store
                GROUP BY hw2.sales.dept, hw2.sales.store) tmp1
            ON tmp2.dept = tmp1.dept AND tmp2.store = tmp1.store
        GROUP BY tmp2.dept
        ORDER BY sum(tmp2.sales / tmp1.size) DESC
        LIMIT 10) tmp5 ON tmp4.dept = tmp5.dept;

